module dvd_rom (
    input  logic [5:0] addr_x,
    input  logic [4:0] addr_y,
    output logic pixel_out
);
    logic [39:0] mem [0:19];

    initial begin
        // La magia del binario: il codice E' il disegno!
        mem[0]  = 40'b0000000000_00000_0000000000_00000_0000000000;
        mem[1]  = 40'b1111111000_00000_1100000011_00000_1111111000;
        mem[2]  = 40'b1111111100_00000_1100000011_00000_1111111100;
        mem[3]  = 40'b1100000110_00000_1100000011_00000_1100000110;
        mem[4]  = 40'b1100000011_00000_0110000110_00000_1100000011;
        mem[5]  = 40'b1100000011_00000_0110000110_00000_1100000011;
        mem[6]  = 40'b1100000011_00000_0110000110_00000_1100000011;
        mem[7]  = 40'b1100000011_00000_0011001100_00000_1100000011;
        mem[8]  = 40'b1100000011_00000_0011001100_00000_1100000011;
        mem[9]  = 40'b1100000011_00000_0011001100_00000_1100000011;
        mem[10] = 40'b1100000011_00000_0001111000_00000_1100000011;
        mem[11] = 40'b1100000011_00000_0001111000_00000_1100000011;
        mem[12] = 40'b1100000110_00000_0001111000_00000_1100000110;
        mem[13] = 40'b1111111100_00000_0000110000_00000_1111111100;
        mem[14] = 40'b1111111000_00000_0000110000_00000_1111111000;
        mem[15] = 40'b0000000000_00000_0000000000_00000_0000000000;
        mem[16] = 40'b0000000000_00000_0000000000_00000_0000000000;
        mem[17] = 40'b0000000000_00000_0000000000_00000_0000000000;
        mem[18] = 40'b0000000000_00000_0000000000_00000_0000000000;
        mem[19] = 40'b0000000000_00000_0000000000_00000_0000000000;
    end

    // Legge il bit corretto (da sinistra verso destra)
    assign pixel_out = mem[addr_y][39 - addr_x];
endmodule